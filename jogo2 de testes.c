void moverMonstroAleatorio() {
    int x, y;
    while(faseatual == 2){
        for (y = 0; y < TAM_F2; y++) {
            for (x = 0; x < TAM_F2; x++) {
                if (mapa[faseatual][y][x] == 'X') {
                    int dir = rand() % 4;
                    int novoX = x;
                    int novoY = y;
    
                    if (dir == 0) novoY--;
                    else if (dir == 1) novoY++;
                    else if (dir == 2) novoX--;
                    else if (dir == 3) novoX++;
    
                    if (mapa[faseatual][novoY][novoX] == ' ' || mapa[faseatual][novoY][novoX] == '&') {
                        if (mapa[faseatual][novoY][novoX] == '&') {
                            printf("O monstro te pegou!\n");
                            reinicios++;
                            if (reinicios >= 3) {
                                printf("Reiniciou 3 vezes. Voltando ao menu.\n");
                                exit(0);
                            }
                            system("pause");
                            resetarJogador();
                            return;
                        }
                        mapa[faseatual][novoY][novoX] = 'X';
                        mapa[faseatual][y][x] = ' ';
                        return;
                    }
                }
            }
        }
    }
}

